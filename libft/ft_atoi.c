#include "libft.h"

int		ft_atoi(const char *str)
{
	long long	result;
	signed char	sign;

	sign = 1;
	result = 0;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-')
	{
		sign = -1;
		str++;
	}
	else if (*str == '+')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		result = result * 10 + *str - '0';
		if (result > (result * 10))
			return (sign > 0 ? -1 : 0);
		str++;
	}
	return (result * sign);
}
